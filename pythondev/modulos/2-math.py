import math

'''Numero pi'''
print(math.pi)
print(f"{math.pi:.2f}")

'''Numero de Euler'''
print(math.e)
print(f"{math.e:.2f}")

'''Arredondamento'''
num = 10.2
print(math.ceil(num))
print(math.floor(num))

'''fatorial'''
num2 = int(input('Numero: \n'))
print(math.factorial(num2))

'''potencia'''
print(math.pow(5,5))

'''raiz quadrada'''
print(math.sqrt(169))

'''mdc'''
mdc = math.gdc(20, 100)
print(mdc)

'''logaritmo'''
print(math.log(10))

