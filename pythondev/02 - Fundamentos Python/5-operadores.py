num1 = int(input("Digite o primeiro numero: \n"))
num2 = int(input("Digite o segundo numero: \n"))


'''
Operadores Aritméticos
'''
sum = num1 + num2
sub = num1 - num2
div = num1 / num2
mult = num1 * num2
mod = num1 % num2 # resto
exp = num1 ** num2 # potencia

print(sum)
print(sub)
print(div)
print(mult)
print(mod)
print(exp)

'''
Operadores de Comparação
'''

bigger = num1 > num2
smaller = num1 < num2
equal = num1 == num2
different = num1 != num2
bigger_equal = num1 >= num2
smaller_equal = num1 <= num2

print(bigger)
print(smaller)
print(equal)
print(different)
print(bigger_equal)
print(smaller_equal)

'''
Operadores de Atribuição:
'''

num1 += 1
print(num1)

num1 -= 1
print(num1)

num1 *= 1
print(num1)

num1 /= 1
print(num1)


